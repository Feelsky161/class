# Создаем первый список из 5 случайных элементов
list1 = [1, 2, 3, 4, 1]  # Пример с повторяющимися значениями

# Создаем второй список из 8 случайных элементов
list2 = [10, 4, 1, 3, 11, 2, 3, 4]  # Пример с повторяющимися значениями

# 1. Найти все уникальные элементы первого списка
unique_elements_list1 = set(list1)
print("Уникальные элементы первого списка:", unique_elements_list1)

# 2. Найти общие элементы для двух списков без повтора
common_elements = list(set(list1).intersection(set(list2)))
print("Общие элементы обоих списков:", common_elements)

# 3. Заменить повторяющиеся значения первого списка на 0
modified_list1 = [0 if x in [1, 10] else x for x in list1]
print("Первый список после замены повторяющихся значений:", modified_list1)

# 4. Определить среднее значение элементов для обоих списков
avg_list1 = sum(modified_list1) / len(modified_list1)
avg_list2 = sum(list2) / len(list2)

print(f"Среднее значение первого списка: {avg_list1}")
print(f"Среднее значение второго списка: {avg_list2}")

if avg_list1 > avg_list2:
    print("Среднее значение больше в первом списке.")
else:
    print("Среднее значение больше во втором списке.")

# 5. Создаем два новых списка одинакового размера с разными значениями
list3 = [10, 20, 30, 40, 50]
list4 = [60, 70, 80, 90, 100]

# Меняем значения списков местами по позициям
for i in range(len(list3)):
    list3[i], list4[i] = list4[i], list3[i]

print("Список 3 после обмена:", list3)
print("Список 4 после обмена:", list4)
